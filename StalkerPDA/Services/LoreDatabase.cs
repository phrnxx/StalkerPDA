                return $"Йди статут читай, чобіт. Зона вільна для всіх!";
            if (replier.Faction == "Бандити" && firstSpeaker.Faction != "Бандити")
            {
                if (triggerPhrase.Contains("Знайшов тіло")) return "Земля бетоном, фраєр. Менше народу - більше кисню.";
