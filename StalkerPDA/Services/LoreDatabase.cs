        public static List<Stalker> GetPossibleRepliers(string faction)
        {
            if (!ReplyMap.TryGetValue(faction, out var names) || names.Count == 0)
                return new List<Stalker>();

            return names
                .Select(name => Characters.FirstOrDefault(c => c.Name == name))
                .Where(c => c != null)
                .ToList();
        }

        public static Stalker GetRandomActiveCharacter()
        {
            var activeChars = Characters.Where(c => c.Name != "Болотний Доктор").ToList();
            return activeChars[_rng.Next(activeChars.Count)];
        }

        public static Stalker GetCharacterByName(string name)
        {
            return Characters.FirstOrDefault(c => c.Name == name) ?? Characters[0];
        }

