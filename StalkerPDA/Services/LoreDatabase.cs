            }
            if (triggerPhrase.Contains("дістав артефакт") || triggerPhrase.Contains("Міняю") || triggerPhrase.Contains("Продам"))
            {
                string[] trades = { "Скільки просиш? Відпиши в приват.", "Даю дві аптечки і цинк 5.45. Згода?", "Дорого береш, брате. Скинь ціну.", "Зараз підійду, тримай товар при собі." };
                return trades[_rng.Next(trades.Length)];
            }
