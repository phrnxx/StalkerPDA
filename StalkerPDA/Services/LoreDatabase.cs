        public static string GenerateDynamicEvent(Stalker author)
        {
            int eventType = _rng.Next(1, 100);
            string loc = GetLocations()[_rng.Next(GetLocations().Count)].Name;
            string mutant = GetMutants()[_rng.Next(GetMutants().Count)].Name;
            string art = GetArtifacts()[_rng.Next(GetArtifacts().Count)].Name;
            string anomaly = GetAnomalies()[_rng.Next(GetAnomalies().Count)].Name;

            if (eventType <= 20)
            {
                Stalker deadStalker = GetRandomActiveCharacter();
                while (deadStalker.Name == author.Name) { deadStalker = GetRandomActiveCharacter(); }

                string[] deathReasons = {
                    $"розірвав {mutant}", $"потрапив у аномалію '{anomaly}'", "зловив маслину від невідомих",
                    "не помітив разтяжку", "загнувся від жорсткої радіації", $"був забитий зграєю, там був {mutant}"
                };
                string reason = deathReasons[_rng.Next(deathReasons.Length)];

                return $"Знайшов тіло. Це {deadStalker.Name} з угруповання {deadStalker.Faction}. Схоже, його {reason} біля локації {loc}. Хабар я забрав, ПДА скидаю в мережу. Земля пухом.";
            }
            else if (eventType > 20 && eventType <= 40)
            {
                string[] warnings = {
                    $"Мужики, не йдіть через {loc}. Там ціла зграя, щонайменше один {mutant}. Ледь ноги виніс.",
                    $"Хто зараз на {loc}? Будьте обережні, бачив як {mutant} потягнув когось у кущі.",
                    $"Увага всім вільним. На {loc} активізувався {mutant}. Потрібна зачистка, скинемось грошима.",
                    $"Біля {loc} з'явилася нова '{anomaly}'. Ледь не влетів. Будьте уважні."
                };
                return warnings[_rng.Next(warnings.Length)];
            }
            else if (eventType > 40 && eventType <= 60)
            {
                string[] loots = {
                    $"Тільки що після викиду на {loc} дістав артефакт '{art}'. Продам на базі, пропонуйте ціну в приват.",
                    $"Міняю '{art}' на цинк патронів 5.45 та пару наукових аптечок. Буду на {loc} ще годину.",
                    $"Знайшов чийсь схрон на {loc}. Дякую за підігрів, братику!",
                    $"Куплю детектор 'Ведмідь' або краще. Можу віддати '{art}' з доплатою."
                };
                return loots[_rng.Next(loots.Length)];
            }
            else if (eventType > 60 && eventType <= 75)
            {
                string[] sos = {
                    $"SOS! Затисли на {loc}. Патронів нуль, навколо {mutant}. Хто поруч - виручайте!",
                    $"Братва, я зловив кулю на {loc}. Кров не зупиняється. У кого є бинти, підійдіть до старої ферми!",
                    $"Терміново потрібен антирад! Застряг на {loc}, фонить страшно. Віддам артефакт за допомогу."
                };
                return sos[_rng.Next(sos.Length)];
            }
            else if (eventType > 75 && eventType <= 90)
            {
                return author.Faction switch
                {
                    "Долг" => $"Загін 'Обов'язку' починає патрулювання сектору {loc}. Цивільним звільнити територію.",
                    "Свобода" => $"Хто знає, довго ще ці клоуни з 'Обов'язку' будуть сидіти на {loc}? Пройти неможливо.",
                    "Бандити" => $"Лохи, хто зараз йде через {loc} — готуйте бабки. Ми чекаємо.",
                    "Варта" => $"Патруль фіксує нелегальну активність у квадраті {loc}. Приготуватися до перевірки документів.",
                    "Екологи" => $"Увага. На {loc} зафіксовано аномальний сплеск. Просимо сталкерів зібрати заміри за винагороду.",
                    _ => $"Знову стрілянина на {loc}. Здається, найманці з кимось зчепилися."
                };
            }
            else
            {
                string[] chatter = {
                    "Хто знає, Сидорович сьогодні приймає хабар чи знову закрився?",
                    "Погода сьогодні просто дрянь. Навіть мутанти по норах сидять.",
                    "Відкрив тушонку, а вона ще з 2012 року. Сподіваюсь, не отруюсь.",
                    "Зона змінюється. Кожен день щось нове."
                };
                return chatter[_rng.Next(chatter.Length)];
            }
        }

