            if (triggerPhrase.Contains("Знайшов тіло"))
            {
                string[] condolences = { "Земля пухом бродязі...", "Хороший був мужик. Зона забирає найкращих.", "От дідько. А він мені сотню винен був.", "Хто б міг подумати... Вчора тільки разом пили." };
